document.addEventListener('DOMContentLoaded', function() {
    var exploreLink = document.getElementById('explore-link');
    var updatesSection = document.getElementById('updates');

    // Event listener for the Explore link
    exploreLink.addEventListener('click', function(event) {
        event.preventDefault(); // Prevent the default link behavior
        updatesSection.scrollIntoView({ behavior: 'smooth' }); // Scroll to the Updates section smoothly
    });
});

